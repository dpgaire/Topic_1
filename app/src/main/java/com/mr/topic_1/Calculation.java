package com.mr.topic_1;

public class Calculation {

    private int first;
    private int second;
    private int result;
    public Calculation(int first, int second) {
        this.first = first;
        this.second = second;
    }

    public int Add(){
        return result= first+second;
    }

    public int Sub(){
        return result= first-second;
    }





    }




